-- Indexes for common queries
CREATE INDEX IF NOT EXISTS idx_news_articles_sector_fetched ON news_articles(sector, fetched_at);
CREATE INDEX IF NOT EXISTS idx_reddit_posts_sector_fetched ON reddit_posts(sector, fetched_at);
CREATE INDEX IF NOT EXISTS idx_sentiment_scores_ticker_scored ON sentiment_scores(ticker, scored_at);
CREATE INDEX IF NOT EXISTS idx_trade_signals_generated ON trade_signals(generated_at);
CREATE INDEX IF NOT EXISTS idx_trades_created ON trades(created_at);
CREATE INDEX IF NOT EXISTS idx_logs_created ON logs(created_at);
